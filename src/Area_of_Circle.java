
/*6. Write a program to enter any radius value of the circle and find out the
*area.(Formula of Area A=PI*r*r).*/

public class Area_of_Circle {       //Declare class

    public static void main(String[] args) {   //Main method or pre defined method

        double PI = 3.14;      //Approximate  value of PI
         int r = 2;            //Assumed radius value
         double Area = PI * r*r;   //Formula for area of circle

        System.out.println("Area of Circle is : " +Area);    // Used String concantination to print result





    }


}
