
 /* 19. Write a Java program to convert a given string into lowercase. Sample
 Input: THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG. */


public class String_Lowercase {           //Declare Class


    public static void main(String[] args) {      //Main method or Pre defined method

        String line = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.";
        System.out.println("UPPERCASE: " + line.toLowerCase());  //Print statement to print in lowercase


    }



}
